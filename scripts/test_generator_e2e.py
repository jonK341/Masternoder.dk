def main():
    import argparse
    p = argparse.ArgumentParser(description="Generator E2E tests")
    p.add_argument("--quick", action="store_true", help="Skip documentary create→progress→video (faster)")
    p.add_argument("--poll", type=int, default=60, metavar="SEC", help="Max seconds to poll for documentary completion (default 60)")
    args = p.parse_args()

    print("Generator E2E tests (Flask test client)")
    if args.quick:
        print("(quick mode: skipping documentary poll)")
    print("=" * 60)
    try:
        client = get_client()
    except Exception as e:
        print(f"FAIL: Could not create test client: {e}")
        import traceback
        traceback.print_exc()
        return 1
    # Reset generator engine before testing so tests start from a clean state
    try:
        r = client.get('/api/generator/reset-for-test?confirm=test')
        if r.status_code == 200:
            data = r.get_json() if r.content_type and 'json' in (r.content_type or '') else {}
            if data and data.get('success'):
                print("  [OK] Generator engine reset (in-memory jobs cleared)")
    except Exception as e:
        print("  [--] Reset failed:", e, "(continuing anyway)")

    tests = [
        ("Generator health (test)", test_generator_health),
        ("Generator jobs list", test_generator_jobs_list),
        ("Generator history", test_generator_history),
        ("Generator statistics", test_generator_statistics),
        ("Generator performance", test_generator_performance),
        ("Unified generate-video (create only)", test_unified_generate_video),
        ("AI-clips create + status", test_ai_clips_create_and_status),
    ]
    if not args.quick:
        tests.append(("Documentary create → progress → video", lambda c: test_documentary_create_progress_video(c, poll_seconds=args.poll)))
    passed = 0
    failed = []
    for name, fn in tests:
        try:
            ok, err = fn(client)
            if ok:
                print(f"  OK   {name}")
                passed += 1
            else:
                print(f"  FAIL {name}: {err}")
                failed.append((name, err))
        except Exception as e:
            print(f"  ERROR {name}: {e}")
            import traceback
            traceback.print_exc()
            failed.append((name, str(e)))

    print("=" * 60)
    print(f"Result: {passed}/{len(tests)} passed")
    if failed:
        print("\nFailed:")
        for name, err in failed:
            print(f"  - {name}: {err}")
        return 1
    print("\nAll generator E2E tests passed.")
    return 0


if __name__ == '__main__':
    sys.exit(main())
