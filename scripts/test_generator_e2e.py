#!/usr/bin/env python3
"""
E2E tests for the generator: documentary create → progress → video, AI-clips, and generator APIs.
Uses Flask test client (no live server). Run from project root: python scripts/test_generator_e2e.py
"""
import os
import sys
import time
import json

_root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
sys.path.insert(0, _root)
os.chdir(_root)

try:
    from dotenv import load_dotenv
    load_dotenv(os.path.join(_root, '.env'))
except Exception:
    pass


def get_client():
    """Create Flask app and return test client. Uses app_context so DB access works."""
    from src.app import create_app
    app = create_app()
    app.config['TESTING'] = True
    client = app.test_client()
    # Keep app and push context so generator_db_service can access db during requests
    client._app = app
    return client


def _get(path, client):
    r = client.get(path)
    try:
        return r.status_code, r.get_json() if r.content_type and 'json' in r.content_type else None
    except Exception:
        return r.status_code, None


def _post(path, client, data=None):
    r = client.post(path, data=json.dumps(data or {}), content_type='application/json')
    try:
        return r.status_code, r.get_json() if r.content_type and 'json' in r.content_type else None
    except Exception:
        return r.status_code, None


def test_generator_health(client):
    """GET /api/generator/test"""
    code, data = _get('/api/generator/test', client)
    if code != 200:
        return False, f"Expected 200, got {code}"
    if not data or not data.get('success'):
        return False, f"success not true: {data}"
    if 'database_available' not in data or 'video_pipeline_available' not in data:
        return False, f"Missing keys: {data}"
    return True, None


def test_generator_history(client):
    """GET /api/generator/history"""
    code, data = _get('/api/generator/history?user_id=test_user&limit=10', client)
    if code != 200:
        return False, f"Expected 200, got {code}"
    if not data or not data.get('success'):
        return False, f"success not true: {data}"
    if 'history' not in data:
        return False, f"Missing history: {data}"
    h = data['history']
    if not isinstance(h, dict) or 'recent' not in h:
        return False, f"history should have total/recent: {h}"
    return True, None


