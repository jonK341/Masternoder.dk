def test_documentary_create_progress_video(client, poll_seconds=60, poll_interval=1.5):
    """
    POST /api/generator/create → poll /api/documentary/progress/<id> until completed/failed
    → GET /api/documentary/video/<id>. Verifies error_message on failure.
    """
    code, data = _post('/api/generator/create', client, {
        'title': 'E2E Test Documentary',
        'description': 'Generated by test_generator_e2e',
        'user_id': 'test_user',
    })
    if code not in (200, 201, 202):
        return False, f"Create returned {code}: {data}"
    if not data or not data.get('success'):
        return False, f"Create success false: {data}"
    doc_id = data.get('documentary_id')
    if not doc_id:
        return False, f"No documentary_id: {data}"

    deadline = time.monotonic() + poll_seconds
    last_status = None
    while time.monotonic() < deadline:
        code, prog = _get(f'/api/documentary/progress/{doc_id}', client)
        if code != 200:
            return False, f"Progress returned {code}: {prog}"
        if not prog or not prog.get('success'):
            return False, f"Progress success false: {prog}"
        last_status = prog.get('status')
        if last_status == 'completed':
            video_url = prog.get('video_url')
            if not video_url:
                return False, "completed but no video_url in progress"
            code2, vid = _get(f'/api/documentary/video/{doc_id}', client)
            if code2 != 200:
                return False, f"Video endpoint returned {code2}"
            if vid and vid.get('status') == 'completed':
                return True, None
            if client.get(f'/api/documentary/video/{doc_id}').status_code == 200:
                return True, None
            return True, None
        if last_status in ('failed', 'error'):
            err = prog.get('error_message') or prog.get('message')
            if err:
                return True, None
            return False, f"Job failed but no error_message: {prog}"
        time.sleep(poll_interval)

    return False, f"Timeout after {poll_seconds}s; last status: {last_status}"


def test_ai_clips_create_and_status(client):
    """POST /api/ai-clips/generate → GET /api/generator/ai-clips/<job_id>"""
    code, data = _post('/api/ai-clips/generate', client, {
        'prompt': 'E2E test clips',
        'user_id': 'test_user',
        'clip_count': 2,
        'duration': 5,
    })
    if code not in (200, 201, 202):
        return False, f"AI-clips create returned {code}: {data}"
    if not data or not data.get('success'):
        return False, f"AI-clips success false: {data}"
    job_id = data.get('job_id')
    if not job_id:
        return False, f"No job_id: {data}"

    code2, status_data = _get(f'/api/generator/ai-clips/{job_id}', client)
    if code2 != 200:
        return False, f"AI-clips status returned {code2}: {status_data}"
    if not status_data or not status_data.get('success'):
        return False, f"AI-clips status success false: {status_data}"
    if 'status' not in status_data:
        return False, f"Missing status: {status_data}"
    return True, None


